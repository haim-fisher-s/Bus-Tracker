"""
conv_lstm is a module that uses Convolution-al LSTM architecture
to predict the future of a time series.

Contains:
    - ConvLSTMInputBuilder
    - ConvLSTMModel
"""


class ConvLSTMInputBuilder:
    """
    This class is used to structure input for training/predicting
    the ConvLSTMModel class.
    """

    # pylint: disable=too-few-public-methods

    def __init__(self):
        """
        Initializes ConvLSTMInputBuilder class.
        """

    @staticmethod
    def start():
        """
        Starts ConvLSTM data contraction process.
        """
        print("ConvLSTM data-contracting started")


class ConvLSTMModel:
    """
    This class is used to structure the ConvLSTM model.
    """

    # pylint: disable=too-few-public-methods

    def __init__(self):
        """
        Initializes ConvLSTMModel class.
        """

    @staticmethod
    def start():
        """
        Starts ConvLSTM model.
        """
        print("ConvLSTM model started")
